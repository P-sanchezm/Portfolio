/**
 * LinkedIn → portfolio timeline importer.
 *
 * This does NOT touch LinkedIn directly. It only reads CSV files that YOU
 * exported manually ("Settings → Data privacy → Get a copy of your data")
 * and placed in public/imports/linkedin/.
 *
 * Output: src/data/importedTimeline.ts (consumed by combinedTimeline.ts).
 *
 * Run via:  npm run import:linkedin   (or tools/import-linkedin.bat)
 */
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import Papa from "papaparse";

import { FILE_CONFIGS } from "./linkedin-field-mapping.js";
import { toEntry, dedupe } from "./normalize-linkedin-data.js";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(__dirname, "..");
const importDir = path.join(root, "public", "imports", "linkedin");
const outputFile = path.join(root, "src", "data", "importedTimeline.ts");

function readCsv(filename) {
  const filePath = path.join(importDir, filename);
  if (!fs.existsSync(filePath)) {
    console.log(`  ·  skipping (not found): ${filename}`);
    return [];
  }

  const csv = fs.readFileSync(filePath, "utf8");
  const parsed = Papa.parse(csv, { header: true, skipEmptyLines: true });

  if (parsed.errors && parsed.errors.length) {
    console.warn(`  !  parse warnings in ${filename}:`, parsed.errors.slice(0, 3));
  }
  return parsed.data || [];
}

function main() {
  console.log("\nLinkedIn import → reading from:");
  console.log(`  ${importDir}\n`);

  if (!fs.existsSync(importDir)) {
    fs.mkdirSync(importDir, { recursive: true });
    console.log("  (created the folder — drop your exported CSVs here and re-run)\n");
  }

  let entries = [];
  for (const config of FILE_CONFIGS) {
    const rows = readCsv(config.file);
    if (!rows.length) continue;
    const mapped = rows
      .map((row) => {
        try {
          return toEntry(row, config);
        } catch (err) {
          console.warn(`  !  skipped a row in ${config.file}:`, err.message);
          return null;
        }
      })
      .filter(Boolean)
      // Drop rows that ended up with no usable organization/title.
      .filter((e) => e.organization !== "Unknown" || e.title);
    console.log(`  ✓  ${config.file}: ${mapped.length} entr${mapped.length === 1 ? "y" : "ies"}`);
    entries = entries.concat(mapped);
  }

  entries = dedupe(entries);

  // Sort newest-first by the year found in `start` (entries without a year last).
  entries.sort((a, b) => {
    const ya = parseInt(String(a.start).match(/\d{4}/)?.[0] ?? "0", 10);
    const yb = parseInt(String(b.start).match(/\d{4}/)?.[0] ?? "0", 10);
    return yb - ya;
  });

  const banner =
    "// AUTO-GENERATED by scripts/import-linkedin-data.js — do not edit by hand.\n" +
    "// Add manual entries in src/data/timeline.ts instead.\n";

  const body =
    `${banner}import type { TimelineEntry } from "../types";\n\n` +
    `export const importedTimeline: TimelineEntry[] = ${JSON.stringify(
      entries,
      null,
      2
    )};\n`;

  fs.writeFileSync(outputFile, body, "utf8");

  console.log(`\n  Wrote ${entries.length} timeline entr${entries.length === 1 ? "y" : "ies"} to:`);
  console.log(`  src/data/importedTimeline.ts\n`);
  if (entries.length === 0) {
    console.log("  No data found. Export your LinkedIn data and place the CSV");
    console.log("  files in public/imports/linkedin/, then run this again.\n");
  } else {
    console.log("  Tip: replace /logos/placeholder-logo.svg with real logos,");
    console.log("  then run `npm run dev` to see them.\n");
  }
}

main();
